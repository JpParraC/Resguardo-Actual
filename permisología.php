// Función para enviar el formulario y mostrar SweetAlert
function enviarFormulario() {
    // Envía el formulario usando AJAX
    $.ajax({
        url: './php/guardar_seleccion.php',
        method: 'POST',
        data: $('#frm').serialize(), // Serializa el formulario
        dataType: 'json', // Esperamos recibir JSON como respuesta
        success: function(response) {
            // Muestra SweetAlert basado en la respuesta JSON recibida
            if (response.success) {
                Swal.fire({
                    icon: 'success',
                    title: 'Éxito',
                    text: response.message
                }).then(function() {
            // Después de que se cierre el SweetAlert, recargar la página
            location.reload();
        });
            } else {
                Swal.fire({
                    icon: 'error',
                    title: 'Error',
                    text: response.message
                });
            }
        },
        error: function() {
            // Muestra SweetAlert en caso de error
            Swal.fire({
                icon: 'error',
                title: 'Error',
                text: 'Ha ocurrido un error al procesar la solicitud.'
            });
        }
    });
}
</script>


